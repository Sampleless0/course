# print functions lol

print('hello world') # ' ' single quote (hello world)
print("hello world") # " " double quote (hello world)

# printing ' ' or " " inside a print function
print("hello 'world'") # hello 'world'
print('hello "world"') # hello "world"

# another way to do that is with \' or \"
print('hello \'world\'') # hello 'world'
print("hello \"world\"") # hello "world"
